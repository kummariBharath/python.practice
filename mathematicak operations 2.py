#augmented Assignment
my_var=110
my_var+=100
print("augmented assignment:",my_var)

my_var2=1000
my_var2-=250
print("augmented assignment subtraction:",my_var2)

#floor division augmented assignment
my_var3=500 
my_var3//=3
print("floor division:",my_var3) 
#pow(),%()
bits=35
bits%=2
print("modulo assignment:",bits)
bits2=44
bits2**=3
print("power assignment:",bits2)
#oct() function converts an integer to an octal string prefixed with "0o"
num = 2026
print(oct(num))
#hex() function converts an integer to a lowercase hexadecimal string prefixed with "0x"
num1 = 2027
print(hex(num1))